CREATE TABLE game (
  id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  game_date_est DATE,
  home_team_id INT,
  visitor_team_id INT,
  season INT,
  team_id_home INT,
  pts_home INT,
  fg_pct_home DECIMAL,
  ft_pct_home DECIMAL,
  fg3_pct_home DECIMAL,
  ast_home INT,
  reb_home INT,
  team_id_away INT,
  pts_away INT,
  fg_pct_away DECIMAL,
  ft_pct_away DECIMAL,
  fg3_pct_away DECIMAL,
  ast_away INT,
  reb_away INT,
  home_team_wins INT
);

CREATE TABLE game_detail (
  game_id INT REFERENCES game(id),
  team_id INT,
  team_abbreviation VARCHAR,
  team_city VARCHAR,
  player_id INT,
  player_name VARCHAR,
  nickname VARCHAR,
  start_position VARCHAR,
  comment VARCHAR,
  minutes VARCHAR,
  fgm INT,
  fga INT,
  fg_pct DECIMAL,
  fg3m INT,
  fg3a INT,
  fg3_pct DECIMAL,
  ftm INT,
  fta INT,
  ft_pct DECIMAL,
  o_reb INT,
  d_reb INT,
  reb INT,
  ast INT,
  stl INT,
  blk INT,
  turnover INT,
  pf INT,
  pts INT,
  plus_minus INT
);